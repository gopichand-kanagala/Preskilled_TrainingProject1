package com.ControlStatements;

public class FORLOOP {

	public static void main(String[] args) {
		
		System.out.println("Main Method Started");
		
		for(int i=1;i<=100;i++) {
			System.out.println("The Loop is:"+i);
		}
		
		
		
		System.out.println("Main Method Ended");
		
	}

}
