package com.Assesments;

public class Assesment1_1Solution {

	public static void main(String[] args) {
		
		int number=10;
		//1.Implicit TypeCasting
		double doubleNumber=number;
		System.out.println("Integer is:"+number);
		System.out.println("double is:"+doubleNumber);
		
		//2.Eclipse TypeCasting
		double doubleNumber2=18.95;
		int number2=(int) doubleNumber2;
		System.out.println("doubleNumber2"+doubleNumber2);
		System.out.println("Integer is:"+number2);
	}

}
